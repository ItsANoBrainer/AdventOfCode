const inputArray = input.split('\n')
const reoccuringWidth = inputArray[0].length
const amountRight = [1,3,5,7,1]
const amountDown = [1,1,1,1,2]
const treeChar = '#'

var totalTreesMultiplied = 1


for(let z = 0; z < amountDown.length; z++) {
    let totalTrees = 0
    let currentColumn = 0
    for(let i = amountDown[z]; i < inputArray.length ; i+=amountDown[z]) {
        const currentRow = inputArray[i]
        const nextColumn = currentColumn+amountRight[z]
        currentColumn = (nextColumn < reoccuringWidth ? nextColumn : nextColumn%reoccuringWidth)
    
        const currentPosChar = currentRow.charAt(currentColumn)
    
        if(currentPosChar == treeChar) { totalTrees++ } 
        //console.log(`${currentRow} ${currentColumn} ${currentPosChar}`)
    }
    totalTreesMultiplied*=totalTrees

    console.log(`You've ran into ${totalTrees} total trees on route ${z+1}!`)
}

console.log(`The product of all total trees that were ran into is: ${totalTreesMultiplied}`)


